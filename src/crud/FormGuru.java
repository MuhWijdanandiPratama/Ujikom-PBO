    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
    if(txt_nip.getText().trim().equals("")){
      JOptionPane.showMessageDialog(null, "NIP belum diisi!");
      txt_nip.requestFocus();
    }else if(txt_nama.getText().trim().equals("")){
      JOptionPane.showMessageDialog(null, "Nama belum diisi!");
      txt_nama.requestFocus();
    }else if(txt_tgl_lahir.getText().trim().equals("")){
      JOptionPane.showMessageDialog(null, "Tanggal lahir belum diisi!");
      txt_tgl_lahir.requestFocus();
    }else if(txt_alamat.getText().trim().equals("")){
      JOptionPane.showMessageDialog(null, "Alamat lahir belum diisi!");
      txt_alamat.requestFocus();
    }else if(txt_mapel.getText().trim().equals("")){
      JOptionPane.showMessageDialog(null, "Mapel belum diisi!");
      txt_mapel.requestFocus();
    }else{
      try{
        crud.setNip(txt_nip.getText());
        crud.setNama(txt_nama.getText());
        crud.setTglLahir(txt_tgl_lahir.getText());
        crud.setAlamat(txt_alamat.getText());
        crud.setMapel(txt_mapel.getText());
        crud.tambah_data(crud.getNip(),crud.getNama(),crud.getTglLahir(),crud.getAlamat(),crud.getMapel());
        tampil_database();
        reset_text();
      }catch(Exception e){}
    }
    }//GEN-LAST:event_btn_simpanActionPerformed

