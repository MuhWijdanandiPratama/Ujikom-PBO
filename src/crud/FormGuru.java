    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
      if(txt_nip.getText().trim().equals("")){
        JOptionPane.showMessageDialog(null,"Maaf,NIP belum diisi!");
        txt_nip.requestFocus();
      } else{
        if(JOptionPane.showConfirmDialog(null, "Apakah Anda yakin menghapus data?","Warning",2)==JOptionPane.YES_OPTION){
          try{
            crud.setNip(txt_nip.getText());
            crud.hapus_data(crud.getNip());
            tampil_database();
            reset_text();
          }catch(Exception e){}
        }
      }
    }//GEN-LAST:event_btn_hapusActionPerformed

