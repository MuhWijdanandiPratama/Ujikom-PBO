  public void tampil_database(){
    Object[] baris = {"NIP","Nama","Tgl Lahir","Alamat","Mapel"};
    tabmode = new DefaultTableModel(null,baris);
    tabel_guru.setModel(tabmode);
    try{
      hasil = crud.tampil_data();
      while(hasil.next()){
        crud.setNip(hasil.getString("nip"));
        crud.setNama(hasil.getString("nama"));
        crud.setTglLahir(hasil.getString("tgl_lahir"));
        crud.setAlamat(hasil.getString("alamat"));
        crud.setMapel(hasil.getString("mapel"));
        String[] data = {crud.getNip(),crud.getNama(),crud.getTglLahir(),crud.getAlamat(),crud.getMapel()};
        tabmode.addRow(data);
      }
    } catch(Exception e){}
  }

